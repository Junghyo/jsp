package z02_vo;

public class Members {
	private String jid;
	private String jpwd;
	private String jname;
	private int jpoint;
	private String jauthor;
	
	public String getJid() {
		return jid;
	}
	public void setJid(String jid) {
		this.jid = jid;
	}
	public String getJpwd() {
		return jpwd;
	}
	public void setJpwd(String jpwd) {
		this.jpwd = jpwd;
	}
	public String getJname() {
		return jname;
	}
	public void setJname(String jname) {
		this.jname = jname;
	}
	public int getJpoint() {
		return jpoint;
	}
	public void setJpoint(int jpoint) {
		this.jpoint = jpoint;
	}
	public String getJauthor() {
		return jauthor;
	}
	public void setJauthor(String jauthor) {
		this.jauthor = jauthor;
	}
	
}
